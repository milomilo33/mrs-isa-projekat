package com.mrsisa.mrsisaprojekat.model;

public abstract class StavkaCenovnika {
	private int sifra,sifraCene;
	
	public StavkaCenovnika() {}

	public int getSifra() {
		return sifra;
	}

	public void setSifra(int sifra) {
		this.sifra = sifra;
	}

	public int getSifraCene() {
		return sifraCene;
	}

	public void setSifraCene(int sifraCene) {
		this.sifraCene = sifraCene;
	}
	
	
	

}
